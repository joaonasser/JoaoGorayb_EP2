import turtle               # Usa a biblioteca de turtle graphics
window = turtle.Screen()    # cria uma janela
window.bgcolor("orange")
window.title("Forca")

tt  = turtle.Turtle()  # Cria um objeto "desenhador"
tt.speed(5)  # define a velocidade
tt.penup()       # Remova e veja o que acontece
tt.setpos(-300,-100)
tt.pendown()
tt.color("purple")




